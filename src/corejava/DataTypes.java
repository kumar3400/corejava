package corejava;

public class DataTypes {
	
	
	public static void main(String []args) {
		
		int myNum = 5;               // integer (whole number)
	    float myFloatNum = 5.99f;    // floating point number
	    char myLetter = 'D';         // character
	    boolean myBool = true;       // boolean
	    String myText = "Hello";     // String    
	    System.out.println(myNum);
	    System.out.println(myFloatNum);
	    System.out.println(myLetter);
	    System.out.println(myBool);
	    System.out.println(myText);
	  
	    //Primitive data types - includes byte, short, int, long, float, double, boolean and char
	    int myNum1 = 100000;//The int data type can store whole numbers from -2147483648 to 2147483647.
	    System.out.println("the integer value is \t"+myNum1);
	    
	    long myNum2=120000000L;//The long data type can store whole numbers from -9223372036854775808 to 9223372036854775807
	    System.out.println("the long value is \t"+myNum2);
	    
	    short myNum3=5000;//The short data type can store whole numbers from -32768 to 32767:
	    System.out.println("the short value is \t"+myNum3);
	    
	    byte myNum4=100;//-128 to 127
	    System.out.println("the byte value is \t"+myNum4);
	    
	    float  myNum5=5.75f;
	    System.out.println("the float value is \t"+myNum5);//The float data type can store fractional numbers from -3.4e038 to 3.4e+038. Note that you should end the value with an "f":
       	    
	    double myNum6=19.99d;
	    System.out.println("the double value is \t"+myNum6);//The double data type can store fractional numbers from 1.7e-308 to 1.7e+038. Note that you should end the value with a "d":

//non-primitive types are Strings, Arrays, Classes, Interface, etc
         


	    
	    
	    



	    

		
	}

}
